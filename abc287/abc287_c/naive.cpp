struct dsu {
  public:
    dsu() : _n(0) {}
    explicit dsu(int n) : _n(n), parent_or_size(n, -1) {}

    int merge(int a, int b) {
        assert(0 <= a && a < _n);
        assert(0 <= b && b < _n);
        int x = leader(a), y = leader(b);
        if (x == y) return x;
        if (-parent_or_size[x] < -parent_or_size[y]) std::swap(x, y);
        parent_or_size[x] += parent_or_size[y];
        parent_or_size[y] = x;
        return x;
    }

    bool same(int a, int b) {
        assert(0 <= a && a < _n);
        assert(0 <= b && b < _n);
        return leader(a) == leader(b);
    }

    int leader(int a) {
        assert(0 <= a && a < _n);
        if (parent_or_size[a] < 0) return a;
        return parent_or_size[a] = leader(parent_or_size[a]);
    }

    int size(int a) {
        assert(0 <= a && a < _n);
        return -parent_or_size[leader(a)];
    }

    std::vector<std::vector<int>> groups() {
        std::vector<int> leader_buf(_n), group_size(_n);
        for (int i = 0; i < _n; i++) {
            leader_buf[i] = leader(i);
            group_size[leader_buf[i]]++;
        }
        std::vector<std::vector<int>> result(_n);
        for (int i = 0; i < _n; i++) {
            result[i].reserve(group_size[i]);
        }
        for (int i = 0; i < _n; i++) {
            result[leader_buf[i]].push_back(i);
        }
        result.erase(
            std::remove_if(result.begin(), result.end(),
                           [&](const std::vector<int>& v) { return v.empty(); }),
            result.end());
        return result;
    }

  private:
    int _n;
    // root node: -1 * component size
    // otherwise: parent
    std::vector<int> parent_or_size;
};

//文字配列の二次元配列みたいなやつを回転させる。
vector<string> rotate90(const vector<string>& matrix) {
    int n = matrix.size();
    int m = matrix[0].size();
    vector<string> rotated(m, string(n, '.'));
    for (int i = 0; i < n; ++i) {
        for (int j = 0; j < m; ++j) {
            rotated[j][n - 1 - i] = matrix[i][j];
        }
    }
    return rotated;
}





// Function to check if the graph forms a single line without cycles
bool isSingleLine(const dsu& d, const vector<vector<int>>& adj, int N) {
    // First, check if all nodes are part of the same connected component
    int representative = d.leader(0); // Assuming node IDs are 0-indexed
    for (int i = 1; i < N; ++i) {
        if (d.leader(i) != representative) {
            return false; // Found nodes in different connected components
        }
    }

    // Next, count the degrees of each node
    int endNodes = 0; // Count of nodes with degree 1
    for (int i = 0; i < N; ++i) {
        int degree = adj[i].size();
        if (degree > 2) return false; // Found a node with degree > 2, not a line
        if (degree == 1) ++endNodes; // Count end nodes
    }

    // For a single line, there must be exactly 2 end nodes
    return endNodes == 2;
}

int main() {
    // Example usage
    int N, M;
    cin >> N >> M;
    dsu d(N);
    vector<vector<int>> adj(N); // Adjacency list to keep track of degrees
    for (int i = 0; i < M; ++i) {
        int u, v;
        cin >> u >> v;
        --u; --v; // Assuming input is 1-indexed, adjust for 0-indexed storage
        d.merge(u, v);
        adj[u].push_back(v);
        adj[v].push_back(u);
    }

    if (isSingleLine(d, adj, N)) {
        cout << "Yes\n";
    } else {
        cout << "No\n";
    }

    return 0;
}