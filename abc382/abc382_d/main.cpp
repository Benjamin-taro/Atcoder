//文字配列の二次元配列みたいなやつを回転させる。
vector<string> rotate90(const vector<string>& matrix) {
    int n = matrix.size();
    int m = matrix[0].size();
    vector<string> rotated(m, string(n, '.'));
    for (int i = 0; i < n; ++i) {
        for (int j = 0; j < m; ++j) {
            rotated[j][n - 1 - i] = matrix[i][j];
        }
    }
    return rotated;
}

// Data structures and algorithms for disjoint set union problems
struct dsu {
  public:
    dsu() : _n(0) {}
    explicit dsu(int n) : _n(n), parent_or_size(n, -1) {}

    int merge(int a, int b) {
        assert(0 <= a && a < _n);
        assert(0 <= b && b < _n);
        int x = leader(a), y = leader(b);
        if (x == y) return x;
        if (-parent_or_size[x] < -parent_or_size[y]) std::swap(x, y);
        parent_or_size[x] += parent_or_size[y];
        parent_or_size[y] = x;
        return x;
    }

    bool same(int a, int b) {
        assert(0 <= a && a < _n);
        assert(0 <= b && b < _n);
        return leader(a) == leader(b);
    }

    int leader(int a) {
        assert(0 <= a && a < _n);
        if (parent_or_size[a] < 0) return a;
        return parent_or_size[a] = leader(parent_or_size[a]);
    }

    int size(int a) {
        assert(0 <= a && a < _n);
        return -parent_or_size[leader(a)];
    }

    std::vector<std::vector<int>> groups() {
        std::vector<int> leader_buf(_n), group_size(_n);
        for (int i = 0; i < _n; i++) {
            leader_buf[i] = leader(i);
            group_size[leader_buf[i]]++;
        }
        std::vector<std::vector<int>> result(_n);
        for (int i = 0; i < _n; i++) {
            result[i].reserve(group_size[i]);
        }
        for (int i = 0; i < _n; i++) {
            result[leader_buf[i]].push_back(i);
        }
        result.erase(
            std::remove_if(result.begin(), result.end(),
                           [&](const std::vector<int>& v) { return v.empty(); }),
            result.end());
        return result;
    }

  private:
    int _n;
    // root node: -1 * component size
    // otherwise: parent
    std::vector<int> parent_or_size;
};

class PrimeSieve {
public:
    PrimeSieve(int max_num) : max_num(max_num), is_prime(max_num + 1, true) {
        run_sieve();
    }

    // 素数判定
    bool isPrime(int num) const {
        if (num < 0 || num > max_num) return false;
        return is_prime[num];
    }

    // 素数のリストを取得
    std::vector<int> getPrimes() const {
        std::vector<int> primes;
        for (int i = 2; i <= max_num; i++) {
            if (is_prime[i]) primes.push_back(i);
        }
        return primes;
    }

private:
    int max_num;
    std::vector<bool> is_prime;

    // エラトステネスの篩を実行
    void run_sieve() {
        is_prime[0] = is_prime[1] = false;
        for (int i = 2; i * i <= max_num; i++) {
            if (is_prime[i]) {
                for (int j = i * i; j <= max_num; j += i) {
                    is_prime[j] = false;
                }
            }
        }
    }
};


vector<vector<int64_t>> ans;
int64_t cnt;

void recursive(vector<int64_t> &prev, int64_t N, int64_t M) {
    // すでに長さ N に達したら、それを答えとして push_back して return
    if ((int)prev.size() == N) {
        // 直前要素が M 以下ならOK
        if (prev.back() <= M) {
            ans.push_back(prev);
            cnt++;
        }
        return;
    }

    // 残り何個入れる必要があるか
    int64_t remain = N - (int)prev.size();

    // 直前要素 (prev が空なら 0 とみなす)
    int64_t last = prev.empty() ? 0 : prev.back();

    // 「次の要素」は最低でも (空なら1)、空でなければ (last + 10)
    int64_t start = prev.empty() ? 1 : (last + 10);

    // 次の要素を x としたとき、そこから先 (remain-1) 個を 10 ずつ足していくので
    // x + 10*(remain-1) <= M であるような x までしか試せない
    int64_t maxNext = M - 10 * (remain - 1);

    // 「スタート」より大きい方を優先
    int64_t low = max(start, (int64_t)1);

    // low > maxNext なら、もう作れないので打ち切り
    if (low > maxNext) {
        return;
    }

    // ここで [low .. maxNext] の範囲をループする
    for (int64_t x = low; x <= maxNext; x++) {
        prev.push_back(x);
        recursive(prev, N, M);
        prev.pop_back();
    }
}

